    private void RegisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegisActionPerformed
        // TODO add your handling code here:
        String nombre = txtNom.getText();
        String direccion = txtDir.getText();
        String apellido = txtApe.getText();
        String celular = txtCel.getText();
        String user = txtUser.getText();
        String contraseña = txtPass1.getText();
        String verificar = txtPass2.getText();
        String cedula = txtCed.getText();
        String barrio = txtBar.getText();
        String telfi = txtTelF.getText();
        String genero = "M";
        if(txtGen.getSelectedIndex()==1){
            genero = "F";
        }
        
        boolean registro = verificar();
        
        if(registro){
            principal.registrarse(cedula, nombre, apellido, genero, direccion, barrio, telfi, celular, user, contraseña);
            Login log = new Login();
            log.setVisible(true);
            log.actualizar(principal);
            JOptionPane.showMessageDialog(null, "Usuario Registrado");
            this.setVisible(false);
        }
    }//GEN-LAST:event_RegisActionPerformed

