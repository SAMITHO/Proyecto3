    public boolean verificar(){
        boolean rta = true;
        boolean existe = principal.clienteExiste(txtUser.getText());
        String salida = "";
        if (txtNom.getText().equals("")|| txtCed.getText().equals("")|| txtCel.getText().equals("") || txtUser.getText().equals("") || txtPass1.getText().equals("") || txtPass2.getText().equals("") || txtDir.getText().equals("")){
        salida += "No ha ingresado los datos completos";          
        rta =false;
        }else if (!txtPass1.getText().equals(txtPass2.getText())){
            salida += "Verificación de contraseña invalida";
            rta = false;
        }
        if(existe){
            salida += "\n \n *Usuario ya existente";
        }
        if(!salida.equals("")){
        JOptionPane.showMessageDialog(null, salida);
        }
        return rta;
    }


